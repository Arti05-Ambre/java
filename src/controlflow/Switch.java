package controlflow;

public class Switch {
    static void main() {
        int a = 8;
        switch(a) {
            case 1:
                System.out.println("one");
                break;
            case 2:
                System.out.println("Two");
                break;
            case 3:
               System.out.println("Three");
               break;
            case 4:
                System.out.println("Four");
                break;
            case 5:
                System.out.println("Five");
                break;
            case 6:
               System.out.println("Six");
               break;
            case 7:
               System.out.println("Seven");
               break;
            default:
                System.out.println("No ans found");

        }
    }
}
